const fs = require('fs');
const path = require('path');

function parseLinks(md, type) {
  const map = {};
  const re = /\[([^\]]+)\]\(\/\w+\/([a-f0-9]{24})\)/g;
  let m;
  while ((m = re.exec(md)) !== null) {
    const name = m[1].replace(/\s*\*\s*$/, '').trim();
    const id = m[2];
    map[id] = name;
  }
  return map;
}

const files = [
  { file: 'AllCountriesLink.md', type: 'country' },
  { file: 'allRegion.md', type: 'region' },
  { file: 'allAlliances.md', type: 'alliance' },
  { file: 'allParties.md', type: 'party' },
  { file: 'allMU.md', type: 'mu' },
];

const out = { countries: {}, regions: {}, alliances: {}, parties: {}, mus: {} };
const keys = ['countries', 'regions', 'alliances', 'parties', 'mus'];

files.forEach((f, i) => {
  const md = fs.readFileSync(path.join(__dirname, f.file), 'utf8');
  out[keys[i]] = parseLinks(md, f.type);
  console.log(`${f.type}: ${Object.keys(out[keys[i]]).length} entries`);
});

let js = '// Auto-generated by buildLookup.js — do not edit\n';
js += 'export const offlineLookups = {\n';
for (const [key, obj] of Object.entries(out)) {
  js += `  ${key}: {\n`;
  for (const [id, name] of Object.entries(obj).sort((a, b) => a[1].localeCompare(b[1]))) {
    js += `    "${id}": ${JSON.stringify(name)},\n`;
  }
  js += '  },\n';
}
js += '};\n';

fs.writeFileSync(path.join(__dirname, 'offlineLookups.js'), js, 'utf8');
console.log('Wrote offlineLookups.js');
